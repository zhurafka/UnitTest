public class Calculator {
    public int getSum(int firstX, int secondX) {     //создали метод суммы
        return firstX + secondX;
    }

    public int getMinus(int firstX, int secondX) {     //создали метод разности
        return firstX - secondX;
    }

    public int getDivision(int firstX, int secondX) {     //создали метод
        return firstX / secondX;
    }




}
